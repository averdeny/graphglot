"""xfail registry for openCypher TCK tests.

Maps test ID patterns to expected failure reasons and categories.
Uses two tiers: feature-prefix globs (all scenarios fail) and
individual test IDs (partial failures within a feature).

Generated by auditing actual parse results against the Neo4j dialect.

Three TCK test suites share this registry:

  test_tck_parse.py      — Can we parse each query?  (XFAIL_PARSE)
  test_tck_roundtrip.py  — Does parse→generate→re-parse + zero scope diagnostics?  (XFAIL_ROUNDTRIP)
  test_tck_errors.py     — Do invalid queries produce errors?  (XFAIL_ERROR)

Current results (2026-04-02):

  Parse:     3897/3897 = 100%   — 0 xfails
  Roundtrip: 3279/3279 = 100%   — 0 xfails
  Error:      540/600  = 90.0%  — 60 xfails (need semantic analysis, not syntax)
"""

from __future__ import annotations

from dataclasses import dataclass
from enum import Enum, auto
from fnmatch import fnmatch


class XFailCategory(Enum):
    """Categories of expected TCK test failures."""

    CYPHER_ONLY = auto()
    """Cypher-specific syntax not supported by GQL parser (e.g., --> arrows)."""
    UNSUPPORTED_FEATURE = auto()
    """Feature not yet implemented (e.g., CALL procedures, pattern comprehensions)."""
    PARSER_BUG = auto()
    """Known parser bug."""
    GENERATOR_BUG = auto()
    """Generator produces incorrect output."""
    SEMANTIC_ONLY = auto()
    """Error requires semantic analysis, not syntactic parsing."""
    LEXER_LIMITATION = auto()
    """Lexer cannot tokenize the input correctly."""
    PARSER_TIMEOUT = auto()
    """Parser hangs on this query (recursion / backtracking)."""


@dataclass(frozen=True)
class XFailEntry:
    """An expected failure entry."""

    reason: str
    category: XFailCategory


